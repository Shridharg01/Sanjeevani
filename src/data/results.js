// Helper function to get all results
export function getAllResults() {
  return [
    ...resultsData.hairTransplant,
    ...resultsData.gfcTreatment,
    ...resultsData.skinTreatments
  ];
}

// Helper function to get featured results
export function getFeaturedResults() {
  return getAllResults().filter(result => result.featured);
}

// Helper function to get results by category
export function getResultsByCategory(category) {
  if (category === 'all') {
    return getAllResults();
  }
  return getAllResults().filter(result => result.category === category);
}

